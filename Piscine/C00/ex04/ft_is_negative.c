#include <unistd.h>

void	ft_is_negative(int n)
{
	char	buffer[2];

	buffer[0] = 'N';
	buffer[1] = 'P';
	if (n < 0)
	{
		write(1, buffer, 1);
	}
	else
	{
		write(1, buffer + 1, 1);
	}
}
