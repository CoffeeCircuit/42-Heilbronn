#include <unistd.h>

void	ft_print_comb(void)
{
	char	buf[4];

	buf[0] = '0';
	buf[1] = '1';
	buf[2] = '2';
	buf[3] = '\0';
	while (buf[0] <= '7')
	{
		write(1, buf, 3);
		if (buf[0] != '7')
			write(1, ", ", 2);
		buf[2] += 1;
		if (buf[2] == 58)
		{
			buf[1] += 1;
			buf[2] = buf[1] + 1;
		}
		if (buf[1] == '9')
		{
			buf[0] += 1;
			buf[1] = buf[0] + 1;
			buf[2] = buf[1] + 1;
		}
	}
}
