#include <unistd.h>

void	ft_print_comb2(void)
{
	int		gr1;
	int		gr2;
	char	temp[2];

	gr1 = 0;
	gr2 = 1;
	while (gr1 < 99)
	{
		temp[0] = '0' + gr1 / 10;
		temp[1] = '0' + gr1 % 10;
		write(1, temp, 2);
		write(1, " ", 1);
		temp[0] = '0' + gr2 / 10;
		temp[1] = '0' + gr2 % 10;
		write(1, temp, 2);
		if (gr1 < 98)
			write(1, ", ", 2);
		if (gr2 == 99)
		{
			gr1++;
			gr2 = gr1;
		}
		gr2++;
	}
}
