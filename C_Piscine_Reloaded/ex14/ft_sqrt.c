int	ft_sqrt(int nb)
{
	int	max_int;
	int	x;

	x = 1;
	max_int = 46340;
	if (nb <= 0)
		return (0);
	while (x < max_int)
	{
		if ((x * x) == nb)
			return (x);
		x++;
	}
	return (0);
}
